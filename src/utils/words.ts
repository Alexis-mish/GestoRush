export function getRandomWords(count: number, difficulty?: 'Fácil' | 'Medio' | 'Difícil', excludeIds?: string[]): WordCard[] {
  let list = [...WORD_DATABASE];
  if (difficulty) {
    list = list.filter(w => w.difficulty === difficulty);
  }
  if (excludeIds && excludeIds.length > 0) {
    const excludeSet = new Set(excludeIds);
    list = list.filter(w => !excludeSet.has(w.id));
  }
  
  // If list is too small, recycle (clear filter)
  if (list.length < count) {
    list = [...WORD_DATABASE];
    if (difficulty) {
      list = list.filter(w => w.difficulty === difficulty);
    }
  }

  // Shuffle list
  for (let i = list.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [list[i], list[j]] = [list[j], list[i]];
  }
  return list.slice(0, count);
}

export function generateTurnCards(excludeIds?: string[]): WordCard[] {
  const faciles = getRandomWords(1, 'Fácil', excludeIds);
  const facilesIds = faciles.map(c => c.id);
  
  const excludeIdsForMedium = excludeIds ? [...excludeIds, ...facilesIds] : facilesIds;
  const medios = getRandomWords(2, 'Medio', excludeIdsForMedium);
  const mediosIds = medios.map(c => c.id);
  
  const excludeIdsForHard = excludeIds ? [...excludeIds, ...facilesIds, ...mediosIds] : [...facilesIds, ...mediosIds];
  const dificiles = getRandomWords(1, 'Difícil', excludeIdsForHard);
  
  const selected = [...faciles, ...medios, ...dificiles];
  return selected.sort(() => Math.random() - 0.5);
}
